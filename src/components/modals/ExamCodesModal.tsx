export const ExamCodesModal: React.FC<ExamCodesModalProps> = ({
  isOpen,
  onClose,
  showToastMsg,
}) => {
  const [selectedExam, setSelectedExam] = useState(EXAMS_LIST[0]);
  const [examCode, setExamCode] = useState<string>('');
  const [isLoading, setIsLoading] = useState<boolean>(false);
  const [copied, setCopied] = useState<boolean>(false);

  useEffect(() => {
    if (!isOpen) return;
    
    const loadCode = async () => {
      setIsLoading(true);
      setCopied(false);
      try {
        const response = await fetch(selectedExam.path);
        if (response.ok) {
          const text = await response.text();
          setExamCode(text);
        } else {
          setExamCode('<!-- تعذر تحميل ملف الكود. يرجى التأكد من وجود الملف في المسار المخصص له. -->');
        }
      } catch (err) {
        console.error(err);
        setExamCode('<!-- حدث خطأ أثناء جلب الكود من الخادم. -->');
      } finally {
        setIsLoading(false);
      }
    };

    loadCode();
  }, [selectedExam, isOpen]);

  if (!isOpen) return null;

  const handleCopy = async () => {
    if (!examCode) return;
    try {
      await navigator.clipboard.writeText(examCode);
      setCopied(true);
      showToastMsg('📋 تم نسخ كود الامتحان بالكامل إلى الحافظة!');
      setTimeout(() => setCopied(false), 2000);
    } catch (err) {
      showToastMsg('❌ فشل النسخ التلقائي. يرجى تظليل الكود ونسخه يدوياً.');
    }
  };

  const handleDownload = () => {
    if (!examCode) return;
    const blob = new Blob([examCode], { type: 'text/html' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${selectedExam.id}.html`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
    showToastMsg('📥 جاري تحميل ملف الامتحان بصيغة HTML!');
  };

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center p-4 bg-slate-900/70 backdrop-blur-md" onClick={onClose}>
      <div 
        className="bg-white dark:bg-gray-900 rounded-3xl max-w-5xl w-full h-[85vh] flex flex-col p-6 shadow-2xl border border-slate-200 dark:border-slate-800 text-right overflow-hidden transition-all"
        onClick={(e) => e.stopPropagation()}
      >
        {/* Header */}
        <div className="flex items-center justify-between border-b border-slate-100 dark:border-slate-800 pb-4 mb-4 flex-row-reverse">
          <div className="flex items-center gap-3">
            <span className="p-2.5 bg-indigo-500/10 text-indigo-500 dark:text-indigo-400 rounded-2xl">
              <Code className="w-6 h-6" />
            </span>
            <div>
              <h3 className="text-xl font-black text-slate-800 dark:text-white">
                لوحة التحكم في نسخ وتحميل أكواد الامتحانات التفاعلية
              </h3>
              <p className="text-xs text-gray-500 dark:text-gray-400 mt-0.5">
                اختر الامتحان من القائمة لنسخ كود الـ HTML والـ JavaScript الخاص به بالكامل
              </p>
            </div>
          </div>
          <button 
            onClick={onClose} 
            className="text-3xl text-gray-400 hover:text-gray-600 dark:hover:text-white transition cursor-pointer select-none"
          >
            &times;
          </button>
        </div>

        {/* Main Body */}
        <div className="flex flex-col lg:flex-row-reverse gap-6 flex-1 overflow-hidden">
          
          {/* Sidebar: Exams list selection */}
          <div className="w-full lg:w-1/3 flex flex-col gap-2 overflow-y-auto pl-1">
            <h4 className="text-sm font-bold text-gray-600 dark:text-gray-400 mb-2 pr-1">قائمة الامتحانات المتاحة</h4>
            {EXAMS_LIST.map((exam) => (
              <button
                key={exam.id}
                onClick={() => setSelectedExam(exam)}
                className={`w-full text-right p-4 rounded-2xl border transition duration-200 text-sm flex flex-col gap-1.5 cursor-pointer ${
                  selectedExam.id === exam.id
                    ? 'bg-gradient-to-l from-indigo-500 to-indigo-600 text-white border-transparent shadow-lg shadow-indigo-500/15'
                    : 'bg-slate-50 hover:bg-slate-100 dark:bg-slate-800/40 dark:hover:bg-slate-800/80 text-gray-700 dark:text-gray-200 border-slate-100 dark:border-slate-800'
                }`}
              >
                <div className="font-extrabold flex items-center gap-2">
                  <span className={selectedExam.id === exam.id ? 'text-white' : 'text-indigo-500'}>📝</span>
                  {exam.title}
                </div>
                <p className={`text-[11px] leading-relaxed ${
                  selectedExam.id === exam.id ? 'text-white/80' : 'text-gray-400 dark:text-gray-500'
                }`}>
                  {exam.description}
                </p>
              </button>
            ))}

            <div className="mt-auto p-4 rounded-2xl bg-amber-500/10 border border-amber-500/20 text-amber-600 dark:text-amber-400 text-xs leading-relaxed">
              <h5 className="font-bold mb-1">💡 طريقة استخدام الكود:</h5>
              أكواد هذه الامتحانات كاملة ومستقلة (Self-contained HTML) تحتوي على التصاميم والأسئلة البرمجية التفاعلية. يمكنك نسخها ولصقها مباشرة في موقعك، أو رفعها كصفحة ويب مستقلة للطلاب لتشغيل الاختبار!
            </div>
          </div>

          {/* Content Pane: Live Code Display & Actions */}
          <div className="flex-1 flex flex-col bg-slate-50 dark:bg-slate-950 rounded-2xl border border-slate-200/55 dark:border-slate-800/60 overflow-hidden">
            
            {/* Action Bar */}
            <div className="flex items-center justify-between p-3 bg-slate-100/75 dark:bg-slate-900 border-b border-slate-200/60 dark:border-slate-800/70">
              <div className="flex items-center gap-1.5 text-xs text-gray-500 dark:text-gray-400 font-mono pr-2">
                <span>📂 {selectedExam.path}</span>
              </div>
              
              <div className="flex items-center gap-2">
                <button
                  onClick={handleDownload}
                  disabled={isLoading || !examCode}
                  className="flex items-center gap-1.5 bg-slate-200 hover:bg-slate-300 dark:bg-slate-800 dark:hover:bg-slate-700 text-slate-800 dark:text-white px-3 py-1.5 rounded-xl text-xs font-bold transition disabled:opacity-50 cursor-pointer"
                  title="تحميل الملف بالكامل"
                >
                  <Download className="w-3.5 h-3.5" />
                  <span>تحميل HTML</span>
                </button>

                <button
                  onClick={handleCopy}
                  disabled={isLoading || !examCode}
                  className={`flex items-center gap-1.5 px-4 py-1.5 rounded-xl text-xs font-bold transition disabled:opacity-50 cursor-pointer ${
                    copied 
                      ? 'bg-emerald-500 text-white' 
                      : 'bg-indigo-600 hover:bg-indigo-700 text-white shadow-md shadow-indigo-500/10'
                  }`}
                  title="نسخ الكود بالكامل"
                >
                  {copied ? <Check className="w-3.5 h-3.5" /> : <Clipboard className="w-3.5 h-3.5" />}
                  <span>{copied ? 'تم نسخ الكود!' : 'نسخ الكود بالكامل'}</span>
                </button>
              </div>
            </div>

            {/* Code editor / pre element */}
            <div className="flex-1 overflow-auto p-4 relative font-mono text-xs text-left select-text" style={{ direction: 'ltr' }}>
              {isLoading ? (
                <div className="absolute inset-0 flex flex-col justify-center items-center bg-slate-50/50 dark:bg-slate-950/50 backdrop-blur-xs gap-3">
                  <div className="w-8 h-8 border-4 border-indigo-500/25 border-t-indigo-600 rounded-full animate-spin" />
                  <span className="text-xs font-bold text-gray-500 dark:text-gray-400">جاري قراءة الكود...</span>
                </div>
              ) : (
                <pre className="text-slate-700 dark:text-slate-300 leading-relaxed whitespace-pre font-mono select-all">
                  {examCode}
                </pre>
              )}
            </div>
          </div>

        </div>
      </div>
    </div>
  );
};
